package Basics;

public class modulasOperator {
    public static void main(String[] args) {
        System.out.println(13%2); // remainder print
        System.out.println(2%13); // if a<b then a is print
        System.out.println(13%-2); // remainder print
        System.out.println(-13%2); // - remainder is print
        System.out.println(-13%-2); // - remainder is print
    }
}
