package Basics;

public class updationVariables {
    // Variables and their declaration
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x = 7;
        System.out.println(x);
        x = x + 8;
        System.out.println(x);
        x = x - 20;
        System.out.println(x);
        x *= -1; // x = x * -1;
        System.out.println(x);
        x /= 5;
        System.out.println(x);
    }
}
